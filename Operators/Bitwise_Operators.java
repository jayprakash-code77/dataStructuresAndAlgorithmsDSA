package dataStructuresAndAlgorithmsDSA.Operators;

public class Bitwise_Operators {
    public static void main(String[] args) {
        /*
        
        1) &
        2) |
        3) ^
        4) ~
        5) <<
        6) >>
        7) >>>
        
         */

        /*
        int a = 5;    // 0101 in binary
        int b = 3;    // 0011 in binary
        int result = a & b; // result is 1 (0001 in binary);
        System.out.println(result);
         */


        /*
        int a = 5;    // 0101 in binary
        int b = 3;    // 0011 in binary
        int result = a | b; // result is 7 (0111 in binary);
        System.out.println(result);
         */


        /*
        int a = 1;    // 0001 in binary
        int b = 2;    // 0010 in binary
        int result = a ^ b; // result is 6 (0011 in binary);
        System.out.println(result);
         */


        /*
        int a = 5;    // 0101 in binary (IMPORTANT)
        int result = ~a; // result is -6 (in binary: 1010, considering 32-bit signed integer representation)
        System.out.println(result);
         */

        /*
        int a = 5;    // 0101 in binary
        int result = a << 1; // result is 10 (1010 in binary)
        System.out.println(result);
         */

        /*
        int a = 5;    // 0101 in binary
        int result = a >> 1; // result is 2 (0010 in binary)

        int b = -5;   // 1111...1011 in binary (32-bit representation)
        int result2 = b >> 1; // result is -3 (1111...1101 in binary)
         */

        /*
        int a = 5;    // 0101 in binary
        int result = a >>> 1; // result is 2 (0010 in binary)

        int b = -5;   // 1111...1011 in binary (32-bit representation)
        int result2 = b >>> 1; // result is 2147483645 (0111...1101 in binary)
         */
















    }
}
